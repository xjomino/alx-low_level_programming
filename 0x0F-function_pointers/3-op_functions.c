#include "3-calc.h"
/**
 * op_add - calculates the sum of two ints
 * @a: first int
 * @b: second int
 * Return: a+b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - calculates the subtraction of two ints
 * @a: first int
 * @b: second int
 * Return: a-b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - calculates the multiplication of two ints
 * @a: first int
 * @b: second int
 * Return: a*b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - calculates the division of two ints
 * @a: first int
 * @b: second int
 * Return: a/b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculates the remainder of two ints
 * @a: first int
 * @b: second int
 * Return: a%b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
